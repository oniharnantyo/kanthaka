                        <h3 class="text-justify mx-4 mx-md-0 mx-lg-0"><strong>Vidyasena sebagai Sekolah</strong></h3>
                        <p class="text-justify mx-4 mx-md-0 mx-lg-0">Secara sederhana Vidyasena terbagi atas tiga level: <i>basic</i>, <i>intermediate</i>, dan <i>advanced</i></p>
                        <p class="text-justify mx-4 mx-md-0 mx-lg-0">
                            <img class="img-fluid float-right ml-3" style="width:400px; " alt="" src={{ url("/images/Dhammatalk.webp") }} />
                            <ul>
                                <li>
                                Untuk level <i>pre basic</i>, Vidyasena dipandang sebagai tempat untuk mencari teman, ajang gaul, tempat pergaulan, dan lain lain. Tentu hal ini tidak salah, namun jika hanya atas alasan mencari teman, maka rasanya rugi karena Vidyasena mempunyai kapasitas yang jauh lebih besar dari sekadar itu.
                                </li>
                                <li>
                                Untuk level <i>basic</i>, Vidyasena menjadi tempat belajar ketrampilan seperti merangkai bunga, mendekorasi, dan juga menjadi tempat praktik ilmu yang didapat, seperti ilmu manajemen, akuntansi, dan sebagainya. Vidyasena juga dapat menjadi tempat belajar berorganisasi.
                                </li>
                                <li>
                                Untuk level <i>intermediate</i>, Vidyasena adalah tempat untuk menempa karakter dan kepribadian. Pengurus yang bergabung dapat merasakan perubahan sifat maupun tingkah laku yang positif apabila mengikuti program dengan sungguh-sungguh.
                                </li>
                                <li>
                                Untuk level tertinggi, <i>advanced</i>, Vidyasena sebagai sekolah Dhamma. Vidyasena menjadi tempat bagi para mahasiswa dan pelajar Buddhis untuk elajar dan praktik Dhamma. Selain mempelajari Dhamma, pengurus dapat menjadi Dhammaduta terpercaya yang dapat menyebarkan harumnya
                                </li>
                            </ul>
                            
                        </p>

